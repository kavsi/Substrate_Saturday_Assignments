// print!, println! problem solution
fn main() {
   /* Fill in the blanks to make it print:
   Hello world, I am 
   Sunface!
   */
    print!("hello world, ");  // used 'print!' the text is printed to the console (io::stdout).
    println!("I am");         // used 'println!' as it is same as print! but a newline is appended.
    println!("Sunface!");     // used 'println!' as it is same as print! but a newline is appended.
}